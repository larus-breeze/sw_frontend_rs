import math

class PCoordSprite():
    def __init__(self, name, scale, xy_coords):
        self.name = name
        self.scale = scale
        self.xy_coords = xy_coords

    def to_l_rad(self, x, y):
        l = self.scale * math.sqrt(x*x + y*y)
        if y == 0:
            if x == 0:
                a = 0.0
            else:
                a = math.pi / 2 * (abs(x)/x)
        elif y < 0 and x >= 0:
            a = math.atan(-x/y)
        elif y > 0 and x >= 0:
            a = math.pi - math.atan(x/y)
        elif y > 0 and x < 0:
            a = math.atan(-x/y) - math.pi
        elif y < 0 and x < 0:
            a = math.atan(-x/y)
        return (l, a)
    
    def __str__(self):
        c_cnt = len(self.xy_coords)
        s = f"pub const {self.name}: [PolarCoordinate; {c_cnt}] = [\n"
        for x, y in self.xy_coords:
            l, a = self.to_l_rad(x, y)
            s += f"    PolarCoordinate{{ len: {l}, alpha: {a} }},\n"
        s += "];\n\n\n"
        return s

class PCoordSprites:
    def __init__(self):
        self.sprites = []

    def add(self, sprite: PCoordSprite):
        self.sprites.append(sprite)

    def __str__(self):
        s = "// Generated by 'create_sprite_coords.py' - do not edit\n\n"
        s += "use super::PolarCoordinate;\n\n"
        for sprite in self.sprites:
            s += str(sprite)
        return s

    def to_file(self, filepath):
        with open(filepath, 'w') as f:
            f.write(str(self))


ARROW = (
    'ARROW_PCOORDS',        # const name                 
    0.01,                   # scale
        [                   # xy coords
            (0, -50),
            (16, -34),
            (5, -34),
            (5, 50),
            (-5, 50),
            (-5, -34),
            (-16, -34)
        ]
)
    
WIND_ARROW = (
    'WIND_ARROW_PCOORDS',   # const name                 
    0.01,                   # scale
        [                   # xy coords
            (0, -66),
            (25, 34),
            (0, 0),
            (-25, 34),
        ]
)
    
sprites = PCoordSprites()
sprites.add(PCoordSprite(*ARROW))
sprites.add(PCoordSprite(*WIND_ARROW))

file_name = 'core/src/view/sprites/polar_defs.rs'
sprites.to_file(file_name)

print(f"'{file_name}' generated")
